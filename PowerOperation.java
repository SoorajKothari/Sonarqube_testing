
public class PowerOperation {

	public PowerOperation() {
		
	}
	
	public void square(double a) {
		System.out.println(""+a+" Square " +" is = "+Math.pow(a, 2));
	}
	
	public void cube(double a) {
		System.out.println(""+a+" Cube " +" is = "+Math.pow(a, 3));
	}
	
	public void power(double a,double b) {
		System.out.println(""+a+" power "+b +" is = "+Math.pow(a, b));
	}
	
	public void squareroot(double a) {
		System.out.println(""+a+" Sqrt " +" is = "+Math.sqrt(a));
	}
}
